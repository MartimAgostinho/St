    /**
     * Restart the DATA timer.
     * 
     * @param key   data timer key (sequence number)
     */
    @Override
    public void restart_data_timer(int key) {
        if (key <0)
            return;
        if (isactive_data_timer(key))
            cancel_data_timer(key);
        start_data_timer(key);
    }
    
    /**
     * Start the DATA timer, associated to key "key"
     *
     * @param key data timer key (sequence number)
     */
    @Override
    public void start_data_timer(int key) {
        try {
            long delay = get_timeout_time();
            if (delay < 0) {
                Log(time + " INVALID start_data_timer: delay must be >= 0\n");
                return;
            }

            Event ev = Event.new_Start_Timer(this, time, key, delay);
            if (key >= 0) {
                    Log(time + " start_data_timer (" + key + "," + delay + ")\n");
            } else {
                Log(time + " INVALID start_data_timer: key must be >= 0\n");
                return;
            }
            data_timer_on.put(key, time);
            String txt = ev.event_to_str();
            conn.send_message(txt);
        } catch (Exception ex) {
            Log("Error in start_timer: " + ex + "\n");
        }
    }

    /**
     * Cancel the DATA timer associated with key "key"
     *
     * @param key   data timer key (sequence number)
     */
    @Override
    public void cancel_data_timer(int key) {
        try {
            Event ev = Event.new_Start_Timer(this, time, key, ACK_TIMER_ID);
            if (key >= 0) {
                if (data_timer_on.containsKey(key)) {
                    data_timer_on.remove(key);
                    Log(time + " cancel_data_timer (" + key + ")\n");
                        
                    String txt = ev.event_to_str();
                    conn.send_message(txt);
                }
            } else {
                Log(time + " INVALID cancel_timer: key must be >= 0\n");
            }
        } catch (Exception ex) {
            Log("Error in cancel_timer: " + ex + "\n");
        }
    }

